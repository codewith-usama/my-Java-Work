package Fundamentals;

public class Operators {
    public static void main(String[] args) {
        // Arithmetic +, -, *, /
        int a =  2, b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // more
        a++;
        System.out.println(a); // increment a by 1
        a--;
        System.out.println(a); // decrement a by 1
        a += b; // adding a, b and assign it in a
        System.out.println(a);
        a -= b; // substracting a, b and assign it in a
        System.out.println(a);

    }
}

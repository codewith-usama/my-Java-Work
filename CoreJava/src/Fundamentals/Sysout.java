package Fundamentals;

public class Sysout {
    public static void main(String[] args) {
        System.out.println("Hello World");
        /*
        System.out.println() this function is used to print statements on console window,
        Whatever be in "" will be printed over console
         */

        System.out.print("Welcome TO Core Java");
        /*
        System.out.println() is first print the statement and then move the cursor to next line.
        Whereas System.out.print() just print the statement and remain on the same line
         */
        System.out.println(" Statement on the same line");
    }
}
